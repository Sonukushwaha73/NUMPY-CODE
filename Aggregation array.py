
#Aggregation----- ke mtlb kishi element ko jodkar ek result niklna hota hai


# np.sum() → sabko add karna

# np.mean() → average niklna

# np.min() → small  element

# np.max() → big element

# np.std() → standard deviation (spread check karne ke liye


import numpy as np
arr = np.array([1,3,4,6])
print("sum:",np.sum(arr))
print("average:",np.average(arr))

print("mean:",np.mean(arr))
print("min:",np.min(arr))
print("max:",np.max(arr))
print("standard deviation:",np.std(arr))



